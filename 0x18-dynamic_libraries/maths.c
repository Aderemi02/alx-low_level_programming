#include "lists.h"
/**
 * add - adding two numbers
 * @a: first number
 * @b: second number
 * Return: result
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - minus two numbers
 * @a: first number
 * @b: second number
 * Return: result
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiply two numbers
 * @a: first number
 * @b: second number
 * Return: result
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divide two numbers
 * @a: first number
 * @b: second number
 * Return: result
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - reminder of two numbers
 * @a: first number
 * @b: second number
 * Return: result
 */
int mod(int a, int b)
{
	return (a % b);
}
