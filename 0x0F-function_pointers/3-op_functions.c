#include "3-calc.h"
/**
 * op_add - adds two integer
 * @a: first integer
 * @b: second integer
 * Return: sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract two integer
 * @a: first integer
 * @b: second integer
 * Return: diff
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply two integer
 * @a: first integer
 * @b: second integer
 * Return: mul
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide two integer
 * @a: first integer
 * @b: second integer
 * Return: div
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - reminder of two integer
 * @a: first integer
 * @b: second integer
 * Return: mod
 */

int op_mod(int a, int b)
{
	return (a % b);
}
